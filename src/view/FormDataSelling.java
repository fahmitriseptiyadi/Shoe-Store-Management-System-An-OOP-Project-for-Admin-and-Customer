    Double Uprice, prodTot = 0.0, GrdTotal = 0.0;
    int AvailQty;
    private void tblShoesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblShoesMouseClicked
        DefaultTableModel model = (DefaultTableModel) tblShoes.getModel();
        int Myindex = tblShoes.getSelectedRow();
        Prid = Integer.valueOf(model.getValueAt(Myindex, 0).toString());
        AvailQty = Integer.valueOf(model.getValueAt(Myindex, 3).toString());
        txtNameBill.setText(model.getValueAt(Myindex, 1).toString());
        Uprice = Double.valueOf(model.getValueAt(Myindex, 4).toString());


    }//GEN-LAST:event_tblShoesMouseClicked

    int i = 0;
    private void BtnAddShoesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnAddShoesMouseClicked
        if (txtNameBill.getText().isEmpty() || txtQty.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Missing Information");

        } else if (AvailQty <= Integer.valueOf(txtQty.getText())) {
            JOptionPane.showMessageDialog(this, "Not Enough In Stock");
        } else {
            i++;
            prodTot = Uprice * Integer.valueOf(txtQty.getText());
            GrdTotal = GrdTotal + prodTot;
            newQty = AvailQty - Integer.valueOf(txtQty.getText());
            if (i == 1) {
                billTxt.setText(billTxt.getText() + "================================= -FOOT FETISH STORE- =================================\n\n"
                        + "\t NUM                   PRODUCT                    PRICE                QUANTITY                TOTAL\n\n\t"
                        + i + "               " + txtNameBill.getText() + "              " + Uprice + "                " + "          " + txtQty.getText() + "               " + "          " + prodTot + "\n");
            } else {
                billTxt.setText(billTxt.getText() + "\n\t" + i + "               " + txtNameBill.getText() + "              " + Uprice + "                " + "          " + txtQty.getText() + "               " + "          " + prodTot + "\n");
            }
            lblGrdTotal.setText("$" + GrdTotal);
            update();

        }
    }//GEN-LAST:event_BtnAddShoesMouseClicked

