    public void GetCat() {
        try {
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/myshoesstoreoop");
            St = Con.createStatement();
            String query = "Select * from category";
            Rs = St.executeQuery(query);
            while (Rs.next()) {
                String Mycat = Rs.getString("name");
                Catcb.addItem(Mycat);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

